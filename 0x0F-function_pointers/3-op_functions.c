#include "3-calc.h"
/**
 * op_add - funtion to add
 * @a: input 1
 * @b: input 2
 * Return: sum a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - funtion to subtract.
 * @a: input 1
 * @b: input 2
 * Return: difference a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - funtion to multiply
 * @a: input 1
 * @b: input 2
 * Return: multiple a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - funtion to divide
 * @a: input 1
 * @b: input 2
 * Return: divisor of  a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - funtion to get a remainder
 * @a: input 1
 * @b: input 2
 * Return: modulas of a and b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
